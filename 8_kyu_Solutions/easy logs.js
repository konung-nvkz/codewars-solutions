function logs(base, A, B) {
    const logA = Math.log(A) / Math.log(base);
    const logB = Math.log(B) / Math.log(base);
    return logA + logB;
}

// Example usage
const base = 10;
const A = 100;
const B = 1000;
const result = logs(base, A, B);
console.log(result); // Output: 5

